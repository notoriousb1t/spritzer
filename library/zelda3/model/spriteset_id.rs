impl SpritesetId {
    pub fn get_room_value(self) -> u8 {
        self as u8 - _ROOM_GFX_OFFSET
    }

    pub fn from_room_value(value: u8) -> Self {
        Self::from_repr(value + _ROOM_GFX_OFFSET).unwrap()
    }
}
